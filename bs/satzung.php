<h1>Satzung
Der Burschenschaft &quot;F&uuml;chse&quot; Fellingshausen</h1>

<h2>&sect;1
Name, Sitz und Rechtsform</h2>
<p>
<ul style="list-style:none;">
<li>1. Der Verein tr&auml;gt den Namen Burschenschaft &quot;F&uuml;chse&quot; Fellingshausen</li>
<li>2. Der Sitz des Vereins ist 35444 Biebertal, Ortsteil Fellingshausen</li>
</ul>
</p>

<h2>&sect;2
Zweck des Vereins</h2>

<p>
<ul style="list-style:none;">
<li>1. Der Verein Burschenschaft &quot;F&uuml;chse&quot; Fellingshausen hat sich zur Aufgabe gestellt:</li>
       <ul style="list-style:none;">
        <li>a) Seine Mitglieder insbesondere die Jugend , zur Pflege der zwischenmenschlichen 
                  und &ouml;rtlichen Gemeinschaft heranzuziehen und zuf&ouml;rdern.</li>
        <li>b) Durch Pflege der Geselligkeit, die Freundschaft und das Zusammengeh&ouml;rigkeitsgef&uuml;hl
             untereinander, die Erhaltung des &ouml;rtlichen Kulturgutes zu st&auml;rken und zu f&ouml;rdern.</li>
       </ul>

<li>2. Der Verein ist auf demokratischer Grundlage aufgebaut.
Politische, Religi&ouml;se oder Rassische Bet&auml;tigungen sind ausgeschlossen.</li>

<li>3. Der Verein betreibt keine Wirtschaftspolitik.</li>

<li>4. Es darf keine Person durch Ausgaben, die dem Zweck des Vereins Fremd sind oder 
            durch unverh&auml;ltnism&auml;&szlig;ig hohe Verg&uuml;tungen beg&uuml;nstigt werden.</li>
</ul>            
</p>


<h2>&sect;3
Mitglieder des Vereins</h2>

<p>Der Verein besteht aus:
<ul style="list-style:none;">
	<li>1. Den Mitgliedern der Burschenschaft</li>
</ul>
</p>


<h2>&sect;4
Erwerb der Mitgliedschaft</h2>
<p>
<ul style="list-style:none;">
<li>1. Die Mitgliedschaft ist schriftlich beim Vorstand zu beantragen und beginnt mit dem Tag der Aufnahme durch den Vorstand. Zur Aufnahme ist die Abgabe einer Eintrittserkl&auml;rung (bei Personen unter achtzehn Jahren zus&auml;tzlich die Unterschrift eines Erziehungsberechtigten) erforderlich.</li>
<li>2. Mitglied der Burschenschaft kann jeder werden der das 15. Lebensjahr vollendet hat.</li>
<li>3. Mitglieder die das 18. Lebensjahr noch nicht vollendet haben m&uuml;ssen sich die Mitgliedschaft von ihren Erziehungsberechtigten genehmigen lassen.</li>
</ul>
</p>

<h2>&sect;5
Ende der Mitgliedschaft</h2>
<p>
<ul style="list-style:none;">
<li>1. Die Mitgliedschaft erlischt:</li>
<ul style="list-style:none;">
<li>a) Mit dem Tod</li>
<li>b) Mit Freiwilligem Austritt</li>
<li>c) Mit dem Ausschluss aus der Burschenschaft</li>
</ul>


<li>2. Der Austritt kann jederzeit erfolgen, er muss dem Vorstand schriftlich mitgeteilt werden.</li>

<li>3. Die Mitgliedschaft endet ferner durch Ausschluss durch den Verein. 
    Der Ausschluss ist auszusprechen, wenn ein Mitglied gegen die Interessen des Vereins             
      verst&ouml;&szlig;t, die Amtsf&auml;higkeit oder bei einem Verhalten, bei dem das Eigentum des 
      Vereins besch&auml;digt oder zerst&ouml;rt wird.</li>

<li>4. &Uuml;ber den Ausschluss der Mitglieder entscheidet der Vorstand.
Er ist dem betreffenden schriftlich mitzuteilen.</li>

<li>5. Gegen den Ausschluss ist innerhalb von zwei Wochen schriftlich beim Vorstand
Einspruch einzulegen.</li>

<li>6. &Uuml;ber den Ausschluss entscheidet der Vorstand endg&uuml;ltig mit Stimmenmehrheit.</li>

<li>7. Ausgeschlossene Mitglieder haben keinerlei Anspruch auf das Verm&ouml;gen oder die
Einrichtungen der Burschenschaft.</li>
</ul>
</p>


<h2>&sect;6
Mittel</h2>

<p>
Die Mittel zur Erreichung des Vereinszwecks werden aufgebracht durch:<br />
<ul style="list-style:none;">
<li>1. J&auml;hrliche Mitgliedsbeitr&auml;ge, deren H&ouml;he, F&auml;lligkeit und zahlungsweise von der
Mitgliederversammlung festzusetzen ist.</li>

<li>2. Freiwillige Zuwendungen.</li>

<li>3. Veranstaltungserl&ouml;se.</li>
</ul>
</p>


<h2>&sect;7
Organe des Vereins</h2>
<p>
         Die Organe des Vereins sind:<br />
<ul style="list-style:none;">
<li>a) die Mitgliederversammlung</li>
<li>b) der Vereinsvorstand</li>
<li>c) die Aussch&uuml;sse</li>
</ul>
</p>
                                                                    
<h2>&sect;8
Rechte und Pflichten</h2>
<p>
         Die Mitglieder haben folgende Rechte:<br />
<ul style="list-style:none;">
<li>1. An allen Versammlungen und Veranstaltungen der Burschenschaft teilzunehmen
und Aufkl&auml;rung in allen Burschenschaftsangelegenheiten zu verlangen,
so weit dies nach Ansicht des Vorstandes im Interesse der Burschenschaft geboten erscheint.</li>

<li>2. Bei Versammlungen Antr&auml;ge und Vorschl&auml;ge zu unterbreiten und etwaige Beschwerden gegen die Beschl&uuml;sse oder Anordnungen des Vorstandes zu f&uuml;hren.</li>

<li>3. Das Wahlrecht.</li>
</ul>
Die Mitglieder haben folgende Pflichten:<br />

<ul style="list-style:none;">
<li>1. Die Satzung sowie Vorstands- und Versammlungsbeschl&uuml;sse zu beachten.</li>

<li>2. Die in der Satzung niedergelegten Grunds&auml;tze zu f&ouml;rdern.</li>

<li>3. Die &uuml;bernommenen &auml;mter gewissenhaft auszuf&uuml;hren.</li>

<li>4. Das zur Benutzung oder Aufbewahrung &uuml;berlassene Burschenschaftseigentum sorgf&auml;ltig und schonend zu behandeln. Mutwillige Besch&auml;digung oder Verlust
ist zu ersetzen.</li>
</ul>
</p>               

<h2>&sect;9
Der Vorstand</h2>
<p>
Der Vorstand besteht aus<br />
<ul style="list-style:none;">
	<li>1.) dem 1.Vorsitzenden</li>
    <li>2.) dem 2.Vorsitzenden</li>
    <li>3.) dem 1.Kassierer</li>
    <li>4.) dem 2.Kassierer</li>
    <li>5.) dem 1.Schriftf&uuml;hrer</li>
    <li>6.) dem 2.Schriftf&uuml;hrer</li>
</ul>
                                          
   
Die Vorstandsmitglieder m&uuml;ssen Vereinsmitglieder sein.<br />

Als gesch&auml;ftsf&uuml;hrender Vorstand wird benannt:<br />
<ul style="list-style:none;">
                                          <li>1.) dem 1.Vorsitzenden</li>
                                          <li>2.) dem 2.Vorsitzenden</li>
                                          <li>3.) dem 1.Kassierer</li>
</ul>
Der Verein wird von 2 Mitgliedern des gesch&auml;ftsf&uuml;hrenden Vorstandes nach
au&szlig;en hin vertreten.<br />

Die Beschl&uuml;sse im Vorstand werden in einer Vorstandssitzung gefasst, die vom
1.Vorsitzenden mit einer Frist von mindestens 7 Tagen schriftlich, m&uuml;ndlich
oder telefonisch einberufen wird.<br />
Der Vorstand ist beschlussf&auml;hig, wenn mindestens 5 Vorstandsmitglieder,
darunter 1 Mitglied des gesch&auml;ftsf&uuml;hrenden, anwesend sind.<br />
Die Beschl&uuml;sse werden mit Stimmenmehrheit gefasst.<br />
Die Sitzung wird vom 1. Vorsitzenden, in dessen Abwesenheit von seinem 
Stellvertreter oder dem 1. Kassierer geleitet.<br />
&Uuml;ber Vorstandssitzung ist ein Protokoll anzufertigen das vom Sitzungsleiter
Und vom Protokollf&uuml;hrer zu unterzeichnen ist.<br />
Dieses Protokoll soll enthalten:<br />
<ul style="list-style:none;">
<li>1.) Ort und Zeit der Sitzung</li>

<li>2.) Namen der Anwesenden Vorstandsmitglieder</li>

<li>3.) Ergebnisse und Abstimmungen</li>

<li>4.) Wortlaut der Beschl&uuml;sse</li>
</ul>
</p>

<h2>&sect;10
Aufgaben der Vorstandsmitglieder</h2>
<p>
<ul style="list-style:none;">
<li>1. Der erste Vorsitzende leitet die Verhandlungen im Vorstand und in den Versammlungen. Er beruft den Vorstand unter Bekanntgabe der Tagesordnung ein, oder die H&auml;lfte des Vorstandes dies beantragt. In der Regel soll monatlich eine Vorstandssitzung statt finden. Der Vorsitzende sorgt f&uuml;r die Ausf&uuml;hrung der Beschl&uuml;sse. Er ist befugt, in dringenden Angelegenheiten selbst&auml;ndig zu handeln, hat jedoch die &uuml;brigen Vorstandsmitglieder von seinen Ma&szlig;nahmen zu verst&auml;ndigen.</li>

<li>2. Der zweite Vorsitzende vertritt den ersten Vorsitzenden in allen Verhinderungsf&auml;llen mit den gleichen Rechten und Pflichten.</li>

<li>3. Der erste Kassierer f&uuml;hrt und verwaltet die Vereinskasse. Er hat &uuml;ber alle Einnahmen und Ausgaben buch zu f&uuml;hren und Belege &uuml;bersichtlich geordnet aufzubewahren. Zahlungen darf er nur mit Genehmigung des 1. Vorsitzenden leisten. Ausgaben, die den Betrag von 500 Euro &uuml;bersteigen bed&uuml;rfen der Zustimmung des Gesamtvorstandes. Der Kassierer ist verpflichtet, den, 1. Vorsitzenden auf Verlangen und dem Vorstand in der ersten Sitzung eines Vierteljahres &uuml;ber den Stand der Kassenverh&auml;ltnisse Auskunft zu erteilen und gegebenenfalls die Belege vorzulegen. Er hat einen Jahresabschluss aufzustellen. Er hat f&uuml;r die p&uuml;nktliche Erhebung des Beitrages, sowie aller sonstigen Forderungen an geldlicher Art zu sorgen.</li>

<li>4. Der zweite Kassierer unterst&uuml;tzt den Kassenwart bei seiner T&auml;tigkeit und vertritt diesen in Verhinderungsf&auml;llen mit gleichen Rechten und Pflichten.</li>

<li>5. Der erste Schriftf&uuml;hrer erledigt s&auml;mtliche schriftlichen Arbeiten und f&uuml;hrt insbesondere die Niederschrift &uuml;ber Verhandlungen des Vorstandes und der Versammlungen. S&auml;mtliche Niederschriften sind von ihm sorgf&auml;ltig aufzubewahren.</li>

<li>6. Der zweite Schriftf&uuml;hrer vertritt den ersten Schriftf&uuml;hrer in Verhinderungsf&auml;llen mit den gleichen Rechten und Pflichten.</li>

<li>7. Der Vorstand kann zur Durchf&uuml;hrung besonderer Aufgaben Sonderaussch&uuml;sse einsetzen. Sie haben grunds&auml;tzlich beratende T&auml;tigkeit.</li>
</ul>
</p>

<h2>&sect;11
Mitgliederversammlung</h2>
<p>
<ul style="list-style:none;">
<li>1. Die Mitgliederversammlung setzt sich aus den Mitgliedern der Burschenschaft zusammen und ist oberstes Beschlussorgan.</li>
<li>2. Die Mitgliederversammlung wird vom Vorsitzenden oder im Verhinderungsfall von seinem Stellvertreter geleitet und ist mindestens einmal j&auml;hrlich unter Bekanntgabe vorgesehener Tagesordnung im Mitteilungsblatt der Gemeinde Biebertal mit einer 7-t&auml;gigen Frist einzuberufen.</li>
<li>3. Antr&auml;ge auf Erg&auml;nzung der Tagesordnung m&uuml;ssen sp&auml;testens vier Tage vor der Mitgliederversammlung dem Vorstand schriftlich mitgeteilt werden, bzw. von mindestens 50% der anwesenden beantragt werden.</li>
<li>4. Auf Antrag von mindestens 30% der Stimmberechtigten ist innerhalb einer vierw&ouml;chigen Frist eine au&szlig;erordentliche Mitgliederversammlung einzuberufen. In dem Antrag m&uuml;ssen die zu behandelnden Tagesordnungspunkte bezeichnet sein.</li>
<li>5. Die Versammlung ist nicht &ouml;ffentlich. Die Zulassung von G&auml;sten, sowie der Presse bedarf der Zustimmung der Mitgliederversammlung.</li>
</ul>
</p>

<h2>&sect;12
Aufgaben der Mitgliederversammlung</h2>
<p>
         Die Aufgaben der Mitgliederversammlung sind insbesondere:<br />
<ul style="list-style:none;">
<li>a) Die Beratung und Beschlussfassung &uuml;ber eingebrachte Antr&auml;ge,</li>
<li>b) Die Wahl des Vorstandes f&uuml;r eine Amtszeit von 2 Jahren; der Vorstand bleibt bis zu einer ordnungsgem&auml;&szlig;en Neuwahl im Amt,</li>
<li>c) Die Festsetzung der Mitgliederbeitr&auml;ge</li>
<li>d) Die Genehmigung des Jahresabschluss</li>
<li>e) Die Entlastung des Vorstandes,</li>
<li>f) Die Wahl von mindestens zwei Kassenpr&uuml;fern,</li>
<li>g) Die Watzwahl</li>
<li>h) Die Beschlussfassung &uuml;ber die Aufl&ouml;sung des Vereins.</li>
</ul>
</p>

<h2>&sect;13
Verfahrensordnung der Mitgliederversammlung</h2>
<p>
<ul style="list-style:none;">
<li>1. Die Mitgliederversammlung ist bei ordnungsgem&auml;&szlig;er Ladung mit dem anwesenden Mitgliedern beschlussf&auml;hig.</li>
<li>2. Bei Wahlen zum Vorstand wird die Versammlungsleitung f&uuml;r die Zeit des Wahlvorganges und der vorhergehenden Diskussion &uuml;ber die Wahl an einen von der Versammlung gew&auml;hlten Wahlausschuss &uuml;bertragen.</li>
<li>3. Die Form der Abstimmung wird vom Versammlungsleiter festgelegt. Auf Antrag mindestens eines Mitgliedes ist geheim abzustimmen.</li>
<li>4. Die Beschl&uuml;sse der Mitgliederversammlung werden im Allgemeinen mit einfacher Mehrheit der abgegebenen g&uuml;ltigen Stimmen.</li>
<li>5. Satzungs&auml;nderungen oder eine Fusion bed&uuml;rfen einer Mehrheit von mindestens zweidrittel der abgegebenen g&uuml;ltigen Stimmen.</li>
<li>6. Der Vorsitzende, der Kassierer, der Schriftf&uuml;hrer und deren Stellvertreter und die Kassenpr&uuml;fer werden von der Mitgliederversammlung gew&auml;hlt. Die Wahlen erfolgen offen, jedoch kann die Mitgliederversammlung in Anlehnung an &sect;13 Absatz 3 dieser Satzung beschlie&szlig;en, die Wahl geheim durchzuf&uuml;hren. Gew&auml;hlt ist, wer die meisten Stimmen auf sich vereinigt.</li>
<li>7. Nicht anwesende Mitglieder k&ouml;nnen nicht gew&auml;hlt werden, es sei denn, dass das Mitglied Vorher befragt wird und dem Vorsitzenden, im Verhinderungsfall von seinem Stellvertreter zu unterzeichnen ist.</li>
</ul>



<h3>Dieses Protokoll soll folgende Punkte erhalten:</h3>
<ul style="list-style:none;">
<li>1. Ort und Zeit der Versammlung</li>
<li>2. Personen des Vorsitzenden der Versammlung</li>
<li>3. Zahl der erschienenen Mitglieder</li>
<li>4. Tagesordnung</li>
<li>5. Art der Ergebnisse der einzelnen Abstimmungen</li>
<li>6. Bei Satzungs&auml;nderungen ist der genaue Wortlaut festzuhalten.</li>
</ul>
</p>


<h2>&sect;14
Rechnungswesen</h2>
<p>
<ul style="list-style:none;">
<li>1. Der 1. Kassierer und sein Stellvertreter sind f&uuml;r die ordnungsgem&auml;&szlig;e Erledigung der Kassengesch&auml;fte verantwortlich.</li>
<li>2. &uuml;ber alle Einnahmen und Ausgaben ist Buch zu f&uuml;hren.</li>
<li>3. Das Gesch&auml;ftsjahr ist das Kalenderjahr.</li>
<li>4. Am Ende des Gesch&auml;ftsjahres legt er gegen&uuml;ber den Kassenpr&uuml;fern Rechnung.</li>
<li>5. Die Kassenpr&uuml;fer pr&uuml;fen die Kassengesch&auml;fte und erstatten der Mitgliederversammlung schriftlich Rechnung.</li>
<li>6. N&auml;heres regelt &sect;10 Absatz 3</li>
</ul>
</p>

<h2>&sect;15
Aufl&ouml;sung des Vereins</h2>
<p>
<ul style="list-style:none;">
<li>1. Die Aufl&ouml;sung des Vereins erfolgt durch den Beschluss der Mitgliederversammlung mit Mehrheit von zweidrittel der abgegebenen g&uuml;ltigen Stimmen. Falls die Versammlung anders beschlie&szlig;t, gelten der 1. und 2. Vorsitzende als Vertretungsberechtigte Liquidationen.<li>
<li>2. Im Falle der Aufl&ouml;sung der Burschenschaft wird in einer letzten Mitgliederversammlung dar&uuml;ber entschieden was mit dem restlichen Verm&ouml;gen passiert.</li>
</ul>
</p>

